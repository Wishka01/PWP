                <h2>Sus inicios:</h2>
                <p>Durante el gobierno del Intendente Pedro Actis Caporale, un grupo de vecinos liderados por el Sr. Ricardo Ávila, comienzan a trabajar en la concreción de esta noble aspiración. El Intendente Caporale apoyó la iniciativa, pero sería durante la gestión de su sucesor: el Dr. Scognamillo, que se concretaría.</p>

                <p>El Dr. Jorge R. Vanossi -quién tanto ha hecho por Pinamar- y, por entonces diputado nacional por la UCR , consigue en noviembre de 1987, que la Honorable Cámara de Diputados de la Nación apruebe una declaración pidiendo al Gobierno Nacional que realice gestiones ante el C.O.N.E.T., para la apertura de una Escuela de Educación Técnica en la ciudad de Pinamar. No sería la única gestión, por este motivo, realizada por este gran amigo de nuestra Ciudad. El 20 de mayo de 1988 el Honorable Concejo Deliberante de Pinamar dirige al Sr. Director de Educación Superior de la Nación, una declaración de respaldo a la nota enviada a esa repartición por el Diputado Nacional Dr. Vanossi.</p>

                <p>El 16 de febrero de 1989 el Consejo Nacional de Educación Técnica aprueba la creación de la E.N.E.T. Nº1 de Pinamar, mediante la Resolución Ministerial Nº 039-C.</p>

                <p>El 21 de abril de 1989 tuvo lugar la inauguración de la Escuela Nacional de Educación Técnica Nº1 de Pinamar, en el ámbito de La Casa de la Cultura de Pinamar, su sede provisoria hasta julio del mismo año.</p>

