                <h2>Nuevo edificio</h2>
                <p>El 26 de agosto del año 2016 se anuncio que la institucion tendria un nuevo edificio, ubicado en la Avenida Inermedanos Sur N654.</p>

                <p>Gracias a la voluntad de la Gobernadora María Eugenia Vidal y sus funcionarios provinciales, y en el marco del Programa Nacional de Refacción Integral de Edificios de Educación Técnico Profesional, la provincia de Buenos Aires llamó a licitación pública para la construcción de las nuevas instalaciones de la escuela técnica de nuestra ciudad.</p>

                <p>El monto presupuestario oficial es de $61.411.258,75, y las empresas participantes tienen tiempo de ofertar hasta el día 20 de septiembre. Las mismas se recibirán en las oficinas del ministerio de la ciudad de La Plata, sita en Calle 8 número 713.</p>

                <p>"Estamos pensando en nuestros chicos y por eso estamos convencidos que la educación puede cambiar el destino de todos los pinamarenses."</p>

                <p>La construcción del nuevo edificio comenzo en el año 2017. Y fue inaugurado el viernes 11 de octubre del 2019, con la presencia del Intendente Martin Yeza, Ricardo Avila (vecino y Concejal de mandato cumplido).</p>

                <p>Con este nuevo edificio se busca duplicar su matrícula estudiantil actual y complementar la excelente calidad educativa de la institución, con la más alta calidad edilicia.</p>
            
